public class Day18_LikeAGifForYourYard
{

  private static final int  SET_0          = 0;

  private static final int  SET_1          = 1;

  private static final int  LIGHT_ON       = 1;

  private static final int  LIGHT_OFF      = 0;

  private static final char CHAR_LIGHT_ON  = '#';

  private static final char CHAR_LIGHT_OFF = '.';

  public static void main( String[] args )
  {
    calculate01( getListProd(), true );

    System.exit( 0 );
  }

  private static void calculate01( List< String > pListInput, boolean pKnzDebug )
  {
    long result_part_01 = 0;

    long result_part_02 = 0;

    long[][][] grid = new long[ 2 ][ 101 ][ 101 ];

    /*
     * ********************************************************************************************
     * Initializing the light from the input list
     * ********************************************************************************************
     */

    setLightStatus( grid, SET_0, 0, 0, 100, 100, LIGHT_OFF );
    setLightStatus( grid, SET_1, 0, 0, 100, 100, LIGHT_OFF );

    int set_write = SET_0;
    int set_read  = SET_1;

    int cur_row = 0;

    for ( String input_str : pListInput )
    {
      if ( !input_str.isBlank() )
      {
        for ( int cur_col = 0; cur_col < input_str.length(); cur_col++ )
        {
          grid[ set_write ][ cur_row ][ cur_col ] = input_str.charAt( cur_col ) == CHAR_LIGHT_ON ? LIGHT_ON : LIGHT_OFF;
        }

        cur_row++;
      }
    }

    /*
     * ********************************************************************************************
     * Doing the animations
     * ********************************************************************************************
     */

    for ( int cur_col = 0; cur_col < 100; cur_col++ )
    {
      if ( set_read == SET_0 )
      {
        set_read  = SET_1;

        set_write = SET_0;
      }
      else
      {
        set_read  = SET_0;

        set_write = SET_1;
      }

      doAnimationPart01( grid, set_read, set_write, 0, 0, 99, 99 );
    }

    result_part_01 = countLightStatus( grid, set_write, 0, 0, 99, 99, LIGHT_ON );

    wl( getDebugLightStatus( grid, set_write, 0, 0, 99, 99 ) );
    wl( "" );
    wl( "Result Part 1 " + result_part_01 );
    wl( "Result Part 2 " + result_part_02 );
    wl( "" );
  }

  private static void doAnimationPart01( long[][][] grid, int pSetRead, int pSetWrite, int pRowStart, int pColStart, int pRowEnd, int pColEnd )
  {
    for ( int cur_row = pRowStart; cur_row <= pRowEnd; cur_row++ )
    {
      for ( int cur_col = pColStart; cur_col <= pColEnd; cur_col++ )
      {
        grid[ pSetWrite ][ cur_row ][ cur_col ] = getNewState( grid, pSetRead, cur_row, cur_col, LIGHT_ON );
      }
    }
  }

  private static long getNewState( long[][][] grid, int pSet, int pRow, int pCol, int pStatus )
  {
    int neighbour_count = 0;

    neighbour_count += getState( grid, pSet, pRow - 1, pCol - 1, pStatus );
    neighbour_count += getState( grid, pSet, pRow - 1, pCol, pStatus );
    neighbour_count += getState( grid, pSet, pRow - 1, pCol + 1, pStatus );

    neighbour_count += getState( grid, pSet, pRow, pCol - 1, pStatus );
    neighbour_count += getState( grid, pSet, pRow, pCol + 1, pStatus );

    neighbour_count += getState( grid, pSet, pRow + 1, pCol - 1, pStatus );
    neighbour_count += getState( grid, pSet, pRow + 1, pCol, pStatus );
    neighbour_count += getState( grid, pSet, pRow + 1, pCol + 1, pStatus );

    long new_status = LIGHT_OFF;

    if ( grid[ pSet ][ pRow ][ pCol ] == LIGHT_ON )
    {
      /*
       * A light which is on stays on when 2 or 3 neighbors are on, and turns off otherwise.
       */
      if ( ( neighbour_count == 2 ) || ( neighbour_count == 3 ) )
      {
        new_status = LIGHT_ON;
      }
    }
    else
    {
      /*
       * A light which is off turns on if exactly 3 neighbors are on, and stays off otherwise.
       */
      if ( neighbour_count == 3 )
      {
        new_status = LIGHT_ON;
      }
    }

    return new_status;
  }

  private static long getState( long[][][] grid, int pSet, int pRow, int pCol, int pStatus )
  {
    try
    {
      return grid[ pSet ][ pRow ][ pCol ] == pStatus ? 1 : 0;
    }
    catch ( Exception e )
    {
      // handle exception
    }

    return 0;
  }

  private static void setLightStatus( long[][][] grid, int pSet, int pRowStart, int pColStart, int pRowEnd, int pColEnd, long pStatus )
  {
    for ( int cur_row = pRowStart; cur_row <= pRowEnd; cur_row++ )
    {
      for ( int cur_col = pColStart; cur_col <= pColEnd; cur_col++ )
      {
        grid[ pSet ][ cur_row ][ cur_col ] = pStatus;
      }
    }
  }

  private static int countLightStatus( long[][][] grid, int pSet, int pRowStart, int pColStart, int pRowEnd, int pColEnd, int pStatus )
  {
    int result_count = 0;

    for ( int cur_row = pRowStart; cur_row <= pRowEnd; cur_row++ )
    {
      for ( int cur_col = pColStart; cur_col <= pColEnd; cur_col++ )
      {
        result_count += grid[ pSet ][ cur_row ][ cur_col ] == pStatus ? 1 : 0;
      }
    }

    return result_count;
  }

  private static String getDebugLightStatus( long[][][] grid, int pSet, int pRowStart, int pColStart, int pRowEnd, int pColEnd )
  {
    String dbg_string = "";

    for ( int cur_row = pRowStart; cur_row <= pRowEnd; cur_row++ )
    {
      for ( int cur_col = pColStart; cur_col <= pColEnd; cur_col++ )
      {
        dbg_string += grid[ pSet ][ cur_row ][ cur_col ] == LIGHT_OFF ? CHAR_LIGHT_OFF : CHAR_LIGHT_ON;
      }

      dbg_string += "\n";
    }

    return dbg_string;
  }

  private static List< String > getListProd()
  {
    List< String > string_array = null;

    String datei_input = "/mnt/hd4tbb/daten/zdownload/advent_of_code_2015__day18_input.txt";

    try
    {
      string_array = Files.readAllLines( Path.of( datei_input ) );
    }
    catch ( IOException e )
    {
      e.printStackTrace();
    }

    return string_array;
  }

  private static void wl( String pString ) // wl = short for "write log"
  {
    System.out.println( pString );
  }
}
